"""Generate the committed Materials Project seed ABox (``mp.ttl``) from the
real, citable facts in ``csv/materials_project.csv``.

This is a one-time **content-authoring** tool (like scripts/make_demo_subset.py),
NOT runtime ingestion code — it runs by hand to refresh the committed ttl when
the CSV changes. The CSV is the source of truth (the same CSV the propose →
materialize dogfood path consumes); this script just projects it into RDF using
the materials_project vocabulary declared in dataset.toml / model.yaml.

Pure stdlib + deterministic output (sorted, fixed formatting) so the committed
mp.ttl diffs cleanly. No generated code is executed (ADR ingestion-execution-safety).

Provenance: the structural facts (mp-id, space group, crystal system) come from
the Materials Project (CC-BY 4.0; A. Jain et al., APL Materials 1, 011002, 2013),
resolved in experiments/mp-linking-poc (link_mp.py --mode live) for the host
phases of Starrydata's thermoelectric samples.

Usage: python build_seed.py   # writes mp.ttl next to this script
"""
from __future__ import annotations

import csv
from pathlib import Path

ONT = "https://kumagallium.github.io/asterism/materials_project/ontology#"
RES = "https://kumagallium.github.io/asterism/materials_project/resource/"
MP_PAGE = "https://next-gen.materialsproject.org/materials/"

HERE = Path(__file__).resolve().parent
CSV_PATH = HERE / "csv" / "materials_project.csv"
TTL_PATH = HERE / "mp.ttl"

HEADER = """\
# Materials Project crystal-structure seed (Asterism example dataset, #19).
#
# GENERATED by build_seed.py from csv/materials_project.csv — do not edit by hand.
#
# Real, citable facts from the Materials Project (CC-BY 4.0; A. Jain et al.,
# APL Materials 1, 011002, 2013), resolved for the host phases of Starrydata's
# thermoelectric samples (experiments/mp-linking-poc, link_mp.py --mode live).
#
# Each Material is keyed by its mp-id and carries mp:formula — the bridge to
# starrydata's sd:compositionString for the cross-dataset structure-property join.

@prefix mp: <https://kumagallium.github.io/asterism/materials_project/ontology#> .
@prefix prov: <http://www.w3.org/ns/prov#> .
@prefix schema: <https://schema.org/> .
@prefix xsd: <http://www.w3.org/2001/XMLSchema#> .

"""

_DATASET_DESC = (
    "Idealized crystal structures (space group / crystal system) for the "
    "host phases of Starrydata's thermoelectric samples."
)

DATASET_NODE = f"""\
<{RES}dataset> a prov:Entity, schema:Dataset ;
    schema:name "Materials Project crystal structures (Asterism example subset)" ;
    schema:description "{_DATASET_DESC}" ;
    schema:license <https://creativecommons.org/licenses/by/4.0/> ;
    schema:citation "A. Jain et al., The Materials Project, APL Materials 1, 011002 (2013)" ;
    schema:url <https://next-gen.materialsproject.org/> .

"""


def _esc(text: str) -> str:
    """Escape a string for a double-quoted Turtle literal."""
    return text.replace("\\", "\\\\").replace('"', '\\"')


def _material_block(row: dict[str, str]) -> str:
    mpid = row["mp_id"].strip()
    formula = row["formula"].strip()
    sym = row["space_group_symbol"].strip()
    num = int(row["space_group_number"].strip())
    system = row["crystal_system"].strip()
    mat = f"{RES}material/{mpid}"
    struct = f"{RES}structure/{mpid}"
    page = f"{MP_PAGE}{mpid}"
    return (
        f"<{mat}> a prov:Entity, mp:Material ;\n"
        f'    mp:mpId "{_esc(mpid)}" ;\n'
        f'    mp:formula "{_esc(formula)}" ;\n'
        f"    schema:url <{page}> ;\n"
        f"    mp:hasCrystalStructure <{struct}> .\n"
        "\n"
        f"<{struct}> a prov:Entity, mp:CrystalStructure ;\n"
        f'    mp:spaceGroupSymbol "{_esc(sym)}" ;\n'
        f"    mp:spaceGroupNumber {num} ;\n"
        f'    mp:crystalSystem "{_esc(system)}" ;\n'
        f"    mp:idealizedFrom <{page}> ;\n"
        f"    mp:ofMaterial <{mat}> .\n"
    )


def main() -> int:
    with CSV_PATH.open(encoding="utf-8") as fh:
        rows = list(csv.DictReader(fh))
    rows.sort(key=lambda r: (r["formula"], r["mp_id"]))
    blocks = [HEADER, DATASET_NODE] + [_material_block(r) + "\n" for r in rows]
    TTL_PATH.write_text("".join(blocks).rstrip() + "\n", encoding="utf-8")
    print(f"wrote {TTL_PATH} ({len(rows)} materials)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
